					$XmlDoc->AddField('newvalue', $this->newvalue->ExportValue($this->Export, $this->ExportOriginalValue));
				}
			}
			$Recordset->MoveNext();
		}
	}

	// Export data in HTML/CSV/Word/Excel/Email/PDF format
	function ExportDocument(&$Doc, &$Recordset, $StartRec, $StopRec, $ExportPageType = "") {
		if (!$Recordset || !$Doc)
			return;

		// Write header
		$Doc->ExportTableHeader();
		if ($Doc->Horizontal) { // Horizontal format, write header
			$Doc->BeginExportRow();
			if ($ExportPageType == "view") {
				$Doc->ExportCaption($this->id);
				$Doc->ExportCaption($this->user);
				$Doc->ExportCaption($this->datetime);
				$Doc->ExportCaption($this->script);
				$Doc->ExportCaption($this->action);
				$Doc->ExportCaption($this->ztable);
				$Doc->ExportCaption($this->zfield);
				$Doc->ExportCaption($this->keyvalue);
				$Doc->ExportCaption($this->oldvalue);
				$Doc->ExportCaption($this->newvalue);
			} else {
				$Doc->ExportCaption($this->id);
				$Doc->ExportCaption($this->user);
				$Doc->ExportCaption($this->datetime);
				$Doc->ExportCaption($this->script);
				$Doc->ExportCaption($this->action);
				$Doc->ExportCaption($this->ztable);
				$Doc->ExportCaption($this->zfield);
				$Doc->ExportCaption($this->keyvalue);
				$Doc->ExportCaption($this->oldvalue);
				$Doc->ExportCaption($this->newvalue);
			}
			if ($this->Export == "pdf") {
				$Doc->EndExportRow(TRUE);
			} else {
				$Doc->EndExportRow();
			}
		}

		// Move to first record
		$RecCnt = $StartRec - 1;
		if (!$Recordset->EOF) {
			$Recordset->MoveFirst();
			if ($StartRec > 1)
				$Recordset->Move($StartRec - 1);
		}
		while (!$Recordset->EOF && $RecCnt < $StopRec) {
			$RecCnt++;
			if (intval($RecCnt) >= intval($StartRec)) {
				$RowCnt = intval($RecCnt) - intval($StartRec) + 1;

				// Page break for PDF
				if ($this->Export == "pdf" && $this->ExportPageBreakCount > 0) {
					if ($RowCnt > 1 && ($RowCnt - 1) % $this->ExportPageBreakCount == 0)
						$Doc->ExportPageBreak();
				}
				$this->LoadListRowValues($Recordset);

				// Render row
				$this->RowType = UP_ROWTYPE_VIEW; // Render view
				$this->ResetAttrs();
				$this->RenderListRow();
				$Doc->BeginExportRow($RowCnt); // Allow CSS styles if enabled
				if ($ExportPageType == "view") {
					$Doc->ExportField($this->id);
					$Doc->ExportField($this->user);
					$Doc->ExportField($this->datetime);
					$Doc->ExportField($this->script);
					$Doc->ExportField($this->action);
					$Doc->ExportField($this->ztable);
					$Doc->ExportField($this->zfield);
					$Doc->ExportField($this->keyvalue);
					$Doc->ExportField($this->oldvalue);
					$Doc->ExportField($this->newvalue);
				} else {
					$Doc->ExportField($this->id);
					$Doc->ExportField($this->user);
					$Doc->ExportField($this->datetime);
					$Doc->ExportField($this->script);
					$Doc->ExportField($this->action);
					$Doc->ExportField($this->ztable);
					$Doc->ExportField($this->zfield);
					$Doc->ExportField($this->keyvalue);
					$Doc->ExportField($this->oldvalue);
					$Doc->ExportField($this->newvalue);
				}
				$Doc->EndExportRow();
			}
			$Recordset->MoveNext();
		}
		$Doc->ExportTableFooter();
	}

	// Row styles
	function RowStyles() {
		$sAtt = "";
		$sStyle = trim($this->CssStyle);
		if (@$this->RowAttrs["style"] <> "")
			$sStyle .= " " . $this->RowAttrs["style"];
		$sClass = trim($this->CssClass);
		if (@$this->RowAttrs["class"] <> "")
			$sClass .= " " . $this->RowAttrs["class"];
		if (trim($sStyle) <> "")
			$sAtt .= " style=\"" . trim($sStyle) . "\"";
		if (trim($sClass) <> "")
			$sAtt .= " class=\"" . trim($sClass) . "\"";
		return $sAtt;
	}

	// Row attributes
	function RowAttributes() {
		$sAtt = $this->RowStyles();
		if ($this->Export == "") {
			foreach ($this->RowAttrs as $k => $v) {
				if ($k <> "class" && $k <> "style" && trim($v) <> "")
					$sAtt .= " " . $k . "=\"" . trim($v) . "\"";
			}
		}
		return $sAtt;
	}

