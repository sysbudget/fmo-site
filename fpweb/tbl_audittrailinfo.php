	// Export data in Xml Format
	function ExportXmlDocument(&$XmlDoc, $HasParent, &$Recordset, $StartRec, $StopRec, $ExportPageType = "") {
		if (!$Recordset || !$XmlDoc)
			return;
		if (!$HasParent)
			$XmlDoc->AddRoot($this->TableVar);

		// Move to first record
		$RecCnt = $StartRec - 1;
		if (!$Recordset->EOF) {
			$Recordset->MoveFirst();
			if ($StartRec > 1)
				$Recordset->Move($StartRec - 1);
		}
		while (!$Recordset->EOF && $RecCnt < $StopRec) {
			$RecCnt++;
			if (intval($RecCnt) >= intval($StartRec)) {
				$RowCnt = intval($RecCnt) - intval($StartRec) + 1;
				$this->LoadListRowValues($Recordset);

				// Render row
				$this->RowType = UP_ROWTYPE_VIEW; // Render view
				$this->ResetAttrs();
				$this->RenderListRow();
				if ($HasParent)
					$XmlDoc->AddRow($this->TableVar);
				else
					$XmlDoc->AddRow();
				if ($ExportPageType == "view") {
					$XmlDoc->AddField('id', $this->id->ExportValue($this->Export, $this->ExportOriginalValue));
					$XmlDoc->AddField('user', $this->user->ExportValue($this->Export, $this->ExportOriginalValue));
					$XmlDoc->AddField('datetime', $this->datetime->ExportValue($this->Export, $this->ExportOriginalValue));
					$XmlDoc->AddField('script', $this->script->ExportValue($this->Export, $this->ExportOriginalValue));
					$XmlDoc->AddField('action', $this->action->ExportValue($this->Export, $this->ExportOriginalValue));
					$XmlDoc->AddField('ztable', $this->ztable->ExportValue($this->Export, $this->ExportOriginalValue));
					$XmlDoc->AddField('zfield', $this->zfield->ExportValue($this->Export, $this->ExportOriginalValue));
					$XmlDoc->AddField('keyvalue', $this->keyvalue->ExportValue($this->Export, $this->ExportOriginalValue));
					$XmlDoc->AddField('oldvalue', $this->oldvalue->ExportValue($this->Export, $this->ExportOriginalValue));
					$XmlDoc->AddField('newvalue', $this->newvalue->ExportValue($this->Export, $this->ExportOriginalValue));
				} else {
					$XmlDoc->AddField('id', $this->id->ExportValue($this->Export, $this->ExportOriginalValue));
					$XmlDoc->AddField('user', $this->user->ExportValue($this->Export, $this->ExportOriginalValue));
					$XmlDoc->AddField('datetime', $this->datetime->ExportValue($this->Export, $this->ExportOriginalValue));
					$XmlDoc->AddField('script', $this->script->ExportValue($this->Export, $this->ExportOriginalValue));
					$XmlDoc->AddField('action', $this->action->ExportValue($this->Export, $this->ExportOriginalValue));
					$XmlDoc->AddField('ztable', $this->ztable->ExportValue($this->Export, $this->ExportOriginalValue));
					$XmlDoc->AddField('zfield', $this->zfield->ExportValue($this->Export, $this->ExportOriginalValue));
					$XmlDoc->AddField('keyvalue', $this->keyvalue->ExportValue($this->Export, $this->ExportOriginalValue));
